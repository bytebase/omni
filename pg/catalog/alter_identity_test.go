func TestAlterAddColumnIdentityRecursesToPartitions(t *testing.T) {
	c := New()
	execIdentitySQL(t, c, `
		CREATE TABLE pt (bucket integer NOT NULL)
			PARTITION BY RANGE (bucket);
		CREATE TABLE pt_p1 PARTITION OF pt FOR VALUES FROM (0) TO (10);
	`)

	execIdentitySQL(t, c, `
		ALTER TABLE pt
			ADD COLUMN id integer GENERATED ALWAYS AS IDENTITY (START WITH 13)
	`)

	parent := c.GetRelation("", "pt")
	child := c.GetRelation("", "pt_p1")
	if parent == nil || child == nil {
		t.Fatal("partitioned table or child not found")
	}
	parentID := parent.Columns[parent.colByName["id"]]
	childID := child.Columns[child.colByName["id"]]
	if parentID.Identity != 'a' || !parentID.NotNull || !parentID.HasDefault {
		t.Fatalf("parent id: got Identity=%q NotNull=%v HasDefault=%v, want identity/default",
			parentID.Identity, parentID.NotNull, parentID.HasDefault)
	}
	if childID.Identity != 'a' || !childID.NotNull {
		t.Fatalf("child id: got Identity=%q NotNull=%v, want inherited identity", childID.Identity, childID.NotNull)
	}
	if _, exists := c.schemaByName["public"].Sequences["pt_p1_id_seq"]; exists {
		t.Fatal("partition child should not get its own identity sequence")
	}
	seq := c.schemaByName["public"].Sequences["pt_id_seq"]
	if seq == nil {
		t.Fatal("identity sequence public.pt_id_seq not found")
	}
	if seq.Start != 13 {
		t.Fatalf("sequence start: got %d, want 13", seq.Start)
	}
}
