func execIdentitySQL(t *testing.T, c *Catalog, sql string) {
	t.Helper()
	stmts := parseStmts(t, sql)
	for _, stmt := range stmts {
		if err := c.ProcessUtility(stmt); err != nil {
			t.Fatalf("ProcessUtility(%q): %v", sql, err)
		}
	}
}

func TestAlterAddIdentityCreatesSequenceAndDefault(t *testing.T) {
	c := New()
	execIdentitySQL(t, c, `CREATE TABLE t (id integer NOT NULL)`)

	execIdentitySQL(t, c, `ALTER TABLE t ALTER COLUMN id ADD GENERATED ALWAYS AS IDENTITY`)

	rel := c.GetRelation("", "t")
	if rel == nil {
		t.Fatal("relation t not found")
	}
	col := rel.Columns[rel.colByName["id"]]
	if col.Identity != 'a' {
		t.Fatalf("identity: got %q, want %q", col.Identity, 'a')
	}
	if !col.HasDefault || !strings.Contains(col.Default, "t_id_seq") {
		t.Fatalf("identity default: got HasDefault=%v Default=%q, want nextval on t_id_seq", col.HasDefault, col.Default)
	}

	schema := c.schemaByName["public"]
	seq := schema.Sequences["t_id_seq"]
	if seq == nil {
		t.Fatal("identity sequence public.t_id_seq not found")
	}
	if seq.OwnerRelOID != rel.OID || seq.OwnerAttNum != col.AttNum {
		t.Fatalf("sequence owner: got rel=%d att=%d, want rel=%d att=%d",
			seq.OwnerRelOID, seq.OwnerAttNum, rel.OID, col.AttNum)
	}
	if seq.TypeOID != INT4OID {
		t.Fatalf("sequence type: got %d, want %d", seq.TypeOID, INT4OID)
	}
}

func TestAlterAddIdentityUsesSequenceNameAndOptions(t *testing.T) {
	c := New()
	execIdentitySQL(t, c, `
		CREATE SCHEMA s;
		CREATE TABLE s.t (id bigint NOT NULL);
	`)

	execIdentitySQL(t, c, `
		ALTER TABLE s.t ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
			SEQUENCE NAME s.custom_identity_seq
			START WITH 42
			INCREMENT BY 2
			CACHE 5
		)
	`)

	rel := c.GetRelation("s", "t")
	if rel == nil {
		t.Fatal("relation s.t not found")
	}
	col := rel.Columns[rel.colByName["id"]]
	if col.Identity != 'd' {
		t.Fatalf("identity: got %q, want %q", col.Identity, 'd')
	}
	if !strings.Contains(col.Default, "s.custom_identity_seq") {
		t.Fatalf("identity default: got %q, want nextval on s.custom_identity_seq", col.Default)
	}

	seq := c.schemaByName["s"].Sequences["custom_identity_seq"]
	if seq == nil {
		t.Fatal("identity sequence s.custom_identity_seq not found")
	}
	if seq.TypeOID != INT8OID {
		t.Fatalf("sequence type: got %d, want %d", seq.TypeOID, INT8OID)
	}
	if seq.Start != 42 || seq.Increment != 2 || seq.CacheValue != 5 {
		t.Fatalf("sequence options: got start=%d increment=%d cache=%d, want 42/2/5",
			seq.Start, seq.Increment, seq.CacheValue)
	}
	if seq.OwnerRelOID != rel.OID || seq.OwnerAttNum != col.AttNum {
		t.Fatalf("sequence owner: got rel=%d att=%d, want rel=%d att=%d",
			seq.OwnerRelOID, seq.OwnerAttNum, rel.OID, col.AttNum)
	}
}

